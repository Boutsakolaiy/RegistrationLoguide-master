package com.example.bout.registrationloguide;

public class UserValues {
    public String name;
    public String surname;
    public String email;
    public String password;
    public String phone;

    public UserValues(String name, String surname, String email, String password, String phone) {
        this.name = name;
        this.surname = surname;
        this.email = email;
        this.password = password;
        this.phone = phone;
    }
}
